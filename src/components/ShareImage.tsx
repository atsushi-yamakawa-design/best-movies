import { useRef, useEffect, useState } from "react";
import style from "./ShareImage.module.scss";

interface ImagePageProps {
  backgroundUrl: string;
  movieImageUrls: string[];
  movieTitles: string[];
}

const ShareImage = ({
  backgroundUrl,
  movieImageUrls,
  movieTitles
}: ImagePageProps) => {
  const canvasRef = useRef<HTMLCanvasElement>(null);

  useEffect(() => {
    if (canvasRef.current) {
      const canvas = canvasRef.current;
      const ctx = canvas.getContext("2d");
      const imagesToLoad = [backgroundUrl, ...movieImageUrls];
      const imagePositions = [
        { x: 0, y: 0 }, // èƒŒæ™¯ç”»åƒ
        { x: 220, y: 380 },
        { x: 220, y: 690 },
        { x: 220, y: 1000 }
      ];

      if (ctx) {
        Promise.all(
          imagesToLoad.map((src) => {
            return new Promise<HTMLImageElement>((resolve) => {
              const img = new Image();
              img.onload = () => resolve(img);
              img.src = src;
            });
          })
        ).then((images) => {
          images.forEach((img, index) => {
            if (index === 0) {
              ctx.drawImage(
                img,
                0,
                0,
                canvas.width,
                (img.naturalHeight / img.naturalWidth) * canvas.width
              );
            } else {
              const ratio = img.naturalHeight / img.naturalWidth;
              const height = 170 * ratio;
              const position = imagePositions[index];
              ctx.drawImage(img, position.x, position.y, 190, height);

              movieTitles.forEach((title, index) => {
                // 3ç•ªç›®ã¾ã§ã®ãƒ†ã‚­ã‚¹ãƒˆ
                ctx.fillStyle = "white";
                ctx.font = "bold 60px Arial";
                if (index < 3) {
                  const position = imagePositions[index + 1];
                  const textX = position.x + 190 + 30;
                  const imageHeight =
                    170 * (img.naturalHeight / img.naturalWidth); // ç”»åƒã®å®Ÿéš›ã®é«˜ã•
                  const textHeight = wrapText(
                    ctx,
                    title,
                    textX,
                    position.y,
                    640,
                    70,
                    true // å®Ÿéš›ã«æç”»ã›ãšã«ãƒ†ã‚­ã‚¹ãƒˆã®é«˜ã•ã‚’è¨ˆç®—
                  );

                  // ãƒ†ã‚­ã‚¹ãƒˆã‚’ç”»åƒã®ä¸­å¤®ã«é…ç½®
                  const textY =
                    position.y + imageHeight / 2 - textHeight / 2 + 40;
                  wrapText(ctx, title, textX, textY, 640, 70); // å®Ÿéš›ã«æç”»
                } else {
                  // 4ç•ªç›®ä»¥é™ã®ãƒ†ã‚­ã‚¹ãƒˆ
                  let startY = 840 + 170 * 3;

                  movieTitles.slice(3).forEach((title, index) => {
                    const dynamicTitle = `${index + 4}. ${title}`;
                    ctx.font = "bold 46px Arial";
                    const textX = 120;
                    let textY = startY;
                    const textHeight = wrapText(
                      ctx,
                      dynamicTitle,
                      textX,
                      textY,
                      960,
                      64 // è¡Œã®é«˜ã•
                    );

                    // æ¬¡ã®ãƒ†ã‚­ã‚¹ãƒˆã®Yåº§æ¨™ã‚’æ›´æ–°
                    startY += textHeight + 24;
                  });
                }
              });
            }
          });
        });
      }
    }
  }, [backgroundUrl, movieImageUrls, movieTitles]);

  // ã‚·ã‚§ã‚¢ç”¨ã®ãƒ†ã‚­ã‚¹ãƒˆç”Ÿæˆ
  const createShareText = (movieTitles: string[]) => {
    let shareText = "My Best Movies 2023\n\n";

    movieTitles.forEach((title, index) => {
      let medal = "";
      switch (index) {
        case 0:
          medal = "ğŸ¥‡ ";
          break;
        case 1:
          medal = "ğŸ¥ˆ ";
          break;
        case 2:
          medal = "ğŸ¥‰ ";
          break;
        default:
          medal = `${index + 1}. `;
          break;
      }
      shareText += `${medal}${title}\n`;
    });

    // ãƒ©ãƒ³ã‚­ãƒ³ã‚°ã®å¾Œã‚ã«æ”¹è¡Œã‚’2ã¤è¿½åŠ 
    shareText += "\n\n";

    // ã‚¯ãƒ¬ã‚¸ãƒƒãƒˆãƒ†ã‚­ã‚¹ãƒˆã®è¿½åŠ 
    let shareTextCredit = "generated by myBestMovie.com\n#myBestMovieOf2023";
    return shareText + shareTextCredit;
  };

  const shareImage = async () => {
    if (navigator.share && canvasRef.current) {
      const canvas = canvasRef.current;
      const shareText = createShareText(movieTitles);
      canvas.toBlob((blob) => {
        if (blob) {
          const file = new File([blob], "image.png", { type: "image/png" });
          navigator
            .share({
              files: [file],
              title: "My Best Movies 2023 ",
              text: shareText
            })
            .catch((error) => {
              console.error("Error sharing the image", error);
            });
        }
      }, "image/png");
    } else {
      console.log(
        "Web Share API is not supported in your browser, or canvas is null."
      );
    }
  };

  return (
    <div className={style.canvasContainer}>
      <canvas ref={canvasRef} width="1179" height="2229" />
      <button onClick={shareImage} className={style.shareButton}>
        Share Image
      </button>
    </div>
  );
};

export default ShareImage;

function wrapText(
  context: CanvasRenderingContext2D,
  text: string,
  x: number,
  y: number,
  maxWidth: number,
  lineHeight: number,
  dryRun: boolean = false
): number {
  let newHeight = 0;
  const words = text.split(" ");
  let line = "";
  let lineCount = 0; // è¡Œæ•°ã‚’ã‚«ã‚¦ãƒ³ãƒˆã™ã‚‹å¤‰æ•°

  for (const word of words) {
    if (lineCount === 2) {
      // 2è¡Œã‚’è¶…ãˆãŸã‚‰çœç•¥è¨˜å·ã‚’è¿½åŠ ã—ã¦å‡¦ç†ã‚’çµ‚äº†
      line += "...";
      if (!dryRun) {
        context.fillText(line, x, y);
      }
      newHeight += lineHeight;
      break;
    }

    // æ—¥æœ¬èªã‹è‹±èªã‹ã«ã‚ˆã£ã¦å‡¦ç†ã‚’å¤‰ãˆã‚‹
    if (isJapanese(word)) {
      // æ—¥æœ¬èªã®å ´åˆã¯æ–‡å­—å˜ä½ã§æ”¹è¡Œ
      for (const char of word) {
        const testLine = line + char;
        if (context.measureText(testLine).width > maxWidth) {
          if (!dryRun) {
            context.fillText(line, x, y);
          }
          line = char;
          y += lineHeight;
          newHeight += lineHeight;
          lineCount++;
          if (lineCount === 2) {
            // 2è¡Œç›®ã«é”ã—ãŸã‚‰æ¬¡ã®ãƒ«ãƒ¼ãƒ—ã§çœç•¥è¨˜å·ã‚’è¿½åŠ 
            break;
          }
        } else {
          line = testLine;
        }
      }
      line += " ";
    } else {
      // è‹±èªã®å˜èªã®å ´åˆã¯å˜èªå˜ä½ã§æ”¹è¡Œ
      const testLine = line + word + " ";
      if (context.measureText(testLine).width > maxWidth) {
        if (!dryRun) {
          context.fillText(line, x, y);
        }
        line = word + " ";
        y += lineHeight;
        newHeight += lineHeight;
        lineCount++;
        if (lineCount === 2) {
          // 2è¡Œç›®ã«é”ã—ãŸã‚‰æ¬¡ã®ãƒ«ãƒ¼ãƒ—ã§çœç•¥è¨˜å·ã‚’è¿½åŠ 
          break;
        }
      } else {
        line = testLine;
      }
    }
  }

  // æœ€å¾Œã®è¡Œã‚’æç”»
  if (!dryRun && line.trim()) {
    context.fillText(line.trim(), x, y);
    newHeight += lineHeight;
  }

  return newHeight;
}

// æ—¥æœ¬èªã‹ã©ã†ã‹ã‚’åˆ¤æ–­ã™ã‚‹ç°¡å˜ãªé–¢æ•°
function isJapanese(text: string) {
  return /[\u3000-\u30FF\uFF00-\uFFEF\u4E00-\u9FAF\u3400-\u4DBF]/.test(text);
}
